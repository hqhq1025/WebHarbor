def _expand_faculty():
    """Expand compact tuples into full Faculty tuples expected by seed_data."""
    full = []
    for name, title, dept, interests in _FAC_RAW:
        last = name.replace('Dr. ', '').split()[-1].lower()
        email = f'{last}.{1 + (sum(ord(c) for c in name) % 99)}@osu.edu'
        bio = (f'{name} serves as {title.lower()} in the {dept}. '
               f'Research and teaching activities focus on {interests.split(",")[0].lower()} '
               f'and related fields. {name.replace("Dr. ", "Professor ")} contributes to '
               f'graduate and undergraduate teaching, mentors PhD students, and collaborates '
               f'with colleagues across Ohio State and external research partners.')
        emeritus = 'Emeritus' in title
        full.append((name, title, dept, email, '', '', interests, bio, emeritus))
    return full


EXTRA_FACULTY = _expand_faculty()


